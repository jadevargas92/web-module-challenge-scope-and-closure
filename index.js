function finalScore(callbackFunction, inningsPlayed){
  let home = 0;
  let away = 0;
  for (let i = 0; i < inningsPlayed; i++) {
    home = home + callbackFunction();
    away = away + callbackFunction();
  }
  return {
    Home: home,
    Away: away
  }
}

